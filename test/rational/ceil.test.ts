import test from 'ava';
import { Rational } from '../../src/';

test('ceil', t => {
  const ceil = (x: any) => new Rational(x).ceil().toString();

  t.is(ceil( 1),                     '1');
  t.is(ceil(-1),                    '-1');
  t.is(ceil( 2),                     '2');

  t.is(ceil(0.1),                    '1');
  t.is(ceil(10),                     '10');

  t.is(ceil(9007199254740992n),      '9007199254740992');
  t.is(ceil(9007199254740994n),      '9007199254740994');
  t.is(ceil(10_000_000_000_000_000n),'10000000000000000');

  t.is(ceil('900719.9254740992'),    '900720');
  t.is(ceil('90071992.54740994'),    '90071993');
  t.is(ceil(10_000_000.00000),       '10000000');
 
  t.is(ceil('-900719.9254740992'),  '-900719');
  t.is(ceil('-90071992.54740994'),  '-90071992');
  t.is(ceil(-10_000_000.00000),     '-10000000');
});
