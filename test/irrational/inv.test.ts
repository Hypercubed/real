import test from 'ava';
import { Irrational } from '../../src/irrational';

test('inv', t => {
  const inv = (x: any) => new Irrational(x).inv().toString();

  t.is(inv(1),                            '1.0000000000000000000e+0');
  t.is(inv(-1),                          '-1.0000000000000000000e+0');
  t.is(inv(2),                            '5.0000000000000000000e-1');

  t.is(inv(0.1),                          '1.0000000000000000000e+1');
  t.is(inv(10),                           '1.0000000000000000000e-1');

  t.is(inv(9007199254740992n),            '1.1102230246251565404e-16');
                                        // 1.1102230246251565404236316680908203125 Ã— 10^-16
  t.is(inv(10_000_000_000_000_000n),      '1.0000000000000000000e-16');
});
