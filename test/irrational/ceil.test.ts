import test from 'ava';
import Irrational from '../../src/irrational';

test('ceil', t => {
  const ceil = (x: any) => new Irrational(x).ceil().toString();

  t.is(ceil( 1),                     '1.0000000000000000000e+0');
  t.is(ceil(-1),                    '-1.0000000000000000000e+0');
  t.is(ceil( 2),                     '2.0000000000000000000e+0');

  t.is(ceil(0.1),                    '1.0000000000000000000e+0');
  t.is(ceil(10),                     '1.0000000000000000000e+1');

  t.is(ceil(9007199254740992n),      '9.0071992547409920000e+15');
  t.is(ceil(9007199254740994n),      '9.0071992547409940000e+15');
  t.is(ceil(10_000_000_000_000_000n),'1.0000000000000000000e+16');

  t.is(ceil('900719.9254740992'),    '9.0072000000000000000e+5');
  t.is(ceil('90071992.54740994'),    '9.0071993000000000000e+7');
  t.is(ceil(10_000_000.00000),       '1.0000000000000000000e+7');
 
  t.is(ceil('-900719.9254740992'),  '-9.0071900000000000000e+5');
  t.is(ceil('-90071992.54740994'),  '-9.0071992000000000000e+7');
  t.is(ceil(-10_000_000.00000),     '-1.0000000000000000000e+7');
});
