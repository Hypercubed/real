import test from 'ava';
import Irrational from '../../src/irrational';

test('log', t => {
  const log = (v: any) => new Irrational(v).log10().toString();

  t.is(log(1),   '0.0000000000000000000e+0');
  t.is(log(10),  '1.0000000000000000000e+0');
  t.is(log(100), '2.0000000000000000000e+0');

  t.is(log(100000), '5.0000000000000000000e+0');

  t.is(log(2),   '3.0102999566398119531e-1');
             // 0.3_01029995663981195213738894724493026768189881462108541310

  t.is(log(2718),   '3.4342494523964755066e+0');
                  // 3.434249452396475506672867756572026433614541038620377722067

  t.is(log(Irrational.E), '4.3429448190325182769e-1');
                      // 0.4_34294481903251827651128918916605082294397005803666566114...

  // t.is(log(9007199254740992n), 15.954589770191003);
  // t.is(log('9007199254740992e11'), 26.954589770191003);
});
